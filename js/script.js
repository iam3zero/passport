//📍 font-control
$(document).ready(function () {

  const baseZoom = 1;     // 기본 배율
  const zoomStep = 0.1;   // 한 번 클릭 시 증가값
  const maxUp = 5;        // 최대 확대 (5단계)
  const maxDown = -2;     // 최대 축소 (2단계)

  let currentStep = 0;

  // 👉 저장된 값 불러오기
  const savedStep = localStorage.getItem('zoomStep');
  if (savedStep !== null) {
    currentStep = parseInt(savedStep);
  }

  applyZoom();

  function applyZoom() {
    const scale = baseZoom + (currentStep * zoomStep);
    $('body').css('zoom', scale);
    localStorage.setItem('zoomStep', currentStep);
  }

  // 🔼 크게
  $('.font_up').on('click', function () {
    if (currentStep < maxUp) {
      currentStep++;
      applyZoom();
    } else {
      alert('더 이상 확대할 수 없습니다.');
    }
  });

  // 🔽 작게
  $('.font_down').on('click', function () {
    if (currentStep > maxDown) {
      currentStep--;
      applyZoom();
    } else {
      alert('더 이상 축소할 수 없습니다.');
    }
  });

  // 🔄 초기화
  $('.font_init').on('click', function () {
    currentStep = 0;
    applyZoom();
  });
});

