//📍 카드 슬라이더 교체 함수

function updateAskSlider(list) {
  const $slider = $('.ask_slider');
  const slideCount = list.length;
  const showCount = 4;

  // slick 제거
  if ($slider.hasClass('slick-initialized')) {
    $slider.slick('unslick');
  }

  // 내용 초기화
  $slider.empty();

  // 새 슬라이드 추가
  list.forEach(item => {
    $slider.append(createSlide(item));
  });

  // slick 재초기화
  $slider.slick({
    slidesToShow: showCount,
    slidesToScroll: 1,
    arrows: true,
    dots: slideCount > showCount,
    infinite: slideCount > showCount,
    draggable: true,
    swipe: true,
    touchThreshold: 20,
    cssEase: 'ease',

     responsive: [
    {
      breakpoint: 1080,
      settings: {
        slidesToShow: 3,
        dots: slideCount > 3,
        infinite: slideCount > 3
      }
    },
    {
      breakpoint: 767,
      settings: {
        slidesToShow: 2
      }
    }
  ]
  });
}


